# Set up Deep Neural Network
class neural_net(nn.Module):
    def __init__(self, layers):
        super(neural_net, self).__init__()

        # Parameters
        # i). Num. of hidden layers
        self.depth = len(layers) - 1

        # ii). activation function
        self.actfunc = nn.Tanh()

        # iii). Initialize the neural network as a list using nn.Modulelist
        self.linears = nn.ModuleList([nn.Linear(layers[i], layers[i+1]) for i in range(self.depth)])

        # iv). initialize the parameters
        for i in range(self.depth):

            # 1. Xavier normalization
            nn.init.xavier_normal_(self.linears[i].weight.data, gain = 5.0/3.0)

            # 2. set biases as zeros
            nn.init.zeros_(self.linears[i].bias.data)

    "foward pass"
    def forward(self, x):

        if torch.is_tensor(x) != True:
            x = torch.from_numpy(x)

        # u_b = torch.from_numpy(ub).float().to(device)
        # l_b = torch.from_numpy(lb).float().to(device)

        #preprocessing input
        # x = (x - l_b)/(u_b - l_b) #feature scaling

        #convert to float
        a = x.float()

        for i in range(self.depth - 1):
            z = self.linears[i](a)
            a = self.actfunc(z)

        a = self.linears[-1](a)

        return a

