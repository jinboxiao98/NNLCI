class MFNN_2D_Riemann():
    def __init__(self, train_input, train_output, layers, lrate, reg):

        #########################################################
        ######### Convert all numpy data to torch tensor ########
        #########################################################

        # ii. input data and training data
        # 1. Input Data
        self.Train_input = torch.tensor(train_input).float().to(device)

        # 2. Training hf Data
        self.Train_hf = torch.tensor(train_output).float().to(device)
        self.layers = layers

        # Call deep neural networks
        self.net_u = neural_net(self.layers).to(device)

        # optimizers Adams
        # 1. Adam optimizer
        self.optimizer_Adam = torch.optim.Adam(self.net_u.parameters(), lr = lrate, weight_decay = reg)

        # 2. start with number of iterations as zero
        self.iter = 0


    def train(self, nIter):
        # Adam Optimizer
        self.net_u.train()
        state_dict = torch.load(root_dir+model_name)
        self.net_u.load_state_dict(state_dict)

        start_time = time.time()
        # I. Adam Optimizer
        for epoch in range(nIter):

           # i). Prediction from neural network
           self.dat_pred = self.net_u(self.Train_input)

           # ii). define loss function
           loss_fcn = torch.nn.MSELoss()
           target = torch.zeros((self.Train_hf.size(0),self.Train_hf.size(1))).to(device)
           #loss = loss_fcn(self.Train_hf,self.dat_pred)
           loss = loss_fcn(self.Train_hf,self.dat_pred)/loss_fcn(self.Train_hf,target)



           # v). backward and optimize
           self.optimizer_Adam.zero_grad()
           loss.backward()
           self.optimizer_Adam.step()

           # start_time = time.time()

           if epoch % 100 == 0:
              elapsed = time.time() - start_time
              print('Number of Iterations: %d, Loss: %.3e, Time Elapsed: %.5f' %
                      (epoch, loss.item(), elapsed))

              start_time = time.time()
              torch.save(self.net_u.state_dict(), root_dir+model_name)


    def predict(self, input_dat_star):

        # 1. Predictive
        self.input_dat_star_o = torch.tensor(input_dat_star).float().to(device)

        # Prediction
        dat_hf_star = self.net_u(self.input_dat_star_o)

        return dat_hf_star

